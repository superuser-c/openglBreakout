void Shader::checkCompileErrors(GLuint object, std::string type)
{
    GLint success;
    char infoLog[1024];
    if (type != "PROGRAM")
    {
        glGetShaderiv(object, GL_COMPILE_STATUS, &success);
        if (!success)
        {
            glGetShaderInfoLog(object, 1024, NULL, infoLog);
			FLog::glogf("Compile-time error: Type: " + type + ";\ninfo log:", 
				"SHADERS", LogType::error);
			FLog::glogf(infoLog, "SHADERS", LogType::raw);
        } else {
			FLog::glogf("Shader compiled: " + type, "SHADERS", LogType::info);
		}
    }
    else
    {
        glGetProgramiv(object, GL_LINK_STATUS, &success);
        if (!success)
        {
            glGetProgramInfoLog(object, 1024, NULL, infoLog);
            FLog::glogf("Compile-time error: Type: LINKING;\ninfo log:", 
				"SHADERS", LogType::error);
			FLog::glogf(infoLog, "SHADERS", LogType::raw);
        } else {
			FLog::glogf("Shaders linked", "SHADERS", LogType::info);
		}
    }
}